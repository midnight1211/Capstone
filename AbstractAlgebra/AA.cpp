    AAResult burnside(const std::vector<Vec> &orbits)
    {
        // Burnside's lemma: |X/G| = (1/|G|) Σ_{g∈G} |Fix(g)|
        // orbits[i] = fixed-point set of group element i
        int G = orbits.size();
        long long totalFixed = 0;
        for (auto &fix : orbits)
            totalFixed += fix.size();
        std::ostringstream ss;
        ss << "Burnside's Lemma  |X/G| = (1/|G|) Σ|Fix(g)|";
        ss << "|G| = " << G << " ";
        for (int i = 0; i < G; ++i)
        {
            ss << "  |Fix(g_" << i << ")| = " << orbits[i].size() << " ";
        }
        long long orbCount = (G > 0) ? totalFixed / G : 0;
        ss << "Σ | Fix(g) | = " << totalFixed << " ";
        ss << "Number of distinct orbits = " << totalFixed << "/" << G << " = " << orbCount;
        return ok(cu_str(orbCount), ss.str());
    }

