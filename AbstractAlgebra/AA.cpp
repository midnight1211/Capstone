    AAResult ringZn(int n)
    {
        std::ostringstream ss;
        ss << "Ring Z_" << n << "\n\n";
        ss << "Units (elements with multiplicative inverse):\n{";
        bool f = true;
        std::vector<int> units;
        for (int k = 1; k < n; ++k)
            if (mcGcd(k, n) == 1)
            {
                if (!f)
                    ss << ",";
                ss << k;
                f = false;
                units.push_back(k);
            }
        ss << "}\nφ(n) = " << units.size() << "\n\n";
        ss << "Zero divisors:\n{";
        f = true;
        for (int k = 1; k < n; ++k)
            if (mcGcd(k, n) > 1)
            {
                if (!f)
                    ss << ",";
                ss << k;
                f = false;
            }
        ss << "}\n\n";
        ss << "Z_n is: " << (units.size() == (size_t)(n - 1) ? "a field (n is prime)" : "not a field");
        if (units.size() == (size_t)(n - 1))
            ss << "\nEvery nonzero element is a unit — this is GF(" << n << ")";
        return ok(ss.str());
    }

    AAResult gaussianIntegers(long long a, long long b)
    {
        long long norm = a * a + b * b;
        std::ostringstream ss;
        ss << "Gaussian Integer z = " << a << "+" << b << "i  ∈ Z[i]\n\n";
        ss << "Norm N(z) = |z|² = " << norm << "\n";
        ss << "Conjugate: z̄ = " << a << "-" << b << "i\n\n";
        // Units in Z[i]: ±1, ±i
        ss << "Units of Z[i]: {1, -1, i, -i}\n";
        ss << "z is a unit iff N(z)=1\n";
        ss << "This element: " << (norm == 1 ? "IS a unit" : "not a unit") << "\n\n";
        // Gaussian primes
        bool prime_n = (norm % 4 == 3 && (long long)std::sqrt(norm) == std::sqrt(norm)) ||
                       (b == 0 && a > 0 && norm % 2 == 1);
        // Simple check: if norm is a rational prime ≡ 3 (mod 4) or norm is 2
        ss << "Gaussian prime test:\n";
        ss << "  N(z) = " << norm << "\n";
        ss << "  z is a Gaussian prime if N(z) is a rational prime p ≡ 3 (mod 4),\n";
        ss << "  or z = u·p where p ≡ 3 (mod 4) is rational prime\n";
        return ok(cu_str(norm), ss.str());
    }

    AAResult isIntegralDomain(int n)
    {
        // Z_n is an integral domain iff n is prime
        bool isPrime = true;
        if (n < 2)
            isPrime = false;
        for (int p = 2; p * p <= n; ++p)
            if (n % p == 0)
            {
                isPrime = false;
                break;
            }
        std::ostringstream ss;
        ss << "Z_" << n << " is " << (isPrime ? "" : "NOT ") << "an integral domain\n";
        ss << "(Z_n is an integral domain iff n is prime)\n\n";
        if (!isPrime)
        {
            ss << "Zero divisors present: e.g. ";
            for (int a = 2; a < n; ++a)
                for (int b = 2; b < n; ++b)
                    if ((1LL * a * b) % n == 0)
                    {
                        ss << a << "·" << b << "≡0 (mod " << n << ")";
                        goto done;
                    }
        done:;
        }
        return ok(isPrime ? "true" : "false", ss.str());
    }

    AAResult isField(int n)
    {
        bool isPrime = n >= 2;
        for (int p = 2; p * p <= n; ++p)
            if (n % p == 0)
            {
                isPrime = false;
                break;
            }
        return ok(isPrime ? "true" : "false", "Z_" + cu_str(n) + (isPrime ? " is a field (n prime)" : " is not a field"));
    }

