    static std::string polyStr(const Poly &p, const std::string &var = "x")
    {
        if (p.empty() || std::all_of(p.begin(), p.end(), [](long long v)
                                     { return v == 0; }))
            return "0";
        std::ostringstream ss;
        bool first = true;
        for (int i = (int)p.size() - 1; i >= 0; --i)
        {
            if (p[i] == 0)
                continue;
            if (!first)
                ss << (p[i] > 0 ? " + " : " - ");
            else if (p[i] < 0)
                ss << "-";
            long long av = std::abs(p[i]);
            if (av != 1 || i == 0)
                ss << av;
            if (i > 0)
                ss << var;
            if (i > 1)
                ss << "^" << i;
            first = false;
        }
        return ss.str();
    }

    static Poly polyNorm(Poly p)
    {
        while (!p.empty() && p.back() == 0)
            p.pop_back();
        return p;
    }

