    AAResult multiplicativeOrder(long long a, long long p)
    {
        // Order of a in GF(p)* = smallest k s.t. a^k ≡ 1 (mod p)
        if (a % p == 0)
            return err("a ≡ 0 (mod p): no multiplicative order");
        long long cur = a % p, k = 1;
        while (cur != 1 && k <= p)
        {
            cur = cur * (a % p) % p;
            k++;
        }
        std::ostringstream ss;
        ss << "Multiplicative order of " << a << " in GF(" << p << ")*\n";
        ss << "ord(" << a << ") = " << k << "\n";
        ss << "(smallest k s.t. " << a << "^k ≡ 1 (mod " << p << "))";
        return ok(cu_str(k), ss.str());
    }

    AAResult primitiveElementGF(int p)
    {
        // Find smallest generator of (Z/pZ)*
        for (int g = 2; g < p; ++g)
        {
            long long cur = g, ord = 1;
            while (cur != 1)
            {
                cur = cur * g % p;
                ord++;
            }
            if (ord == p - 1)
                return ok(cu_str(g), cu_str(g) + " is a primitive root mod " + cu_str(p) + " (order " + cu_str(p - 1) + ")");
        }
        return err("No primitive root found");
    }

