    AAResult cyclic(int n)
    {
        std::ostringstream ss;
        ss << "Cyclic Group Z_" << n << "\n";
        ss << "Order: " << n << "\n\n";
        // Generators: elements coprime to n
        ss << "Generators (elements of order " << n << "): {";
        bool first = true;
        for (int k = 1; k < n; ++k)
            if (mcGcd(k, n) == 1)
            {
                if (!first)
                    ss << ",";
                ss << k;
                first = false;
            }
        ss << "}\n\n";
        // Subgroup structure (by Lagrange: one for each divisor of n)
        ss << "Subgroups (one per divisor of n):\n";
        for (int d = 1; d <= n; ++d)
            if (n % d == 0)
            {
                ss << "  Z_" << d << " = {";
                for (int k = 0; k < n; k += n / d)
                    ss << k << (k + n / d < n ? "," : "");
                ss << "} (generated by " << (n / d) % n << ")\n";
            }
        ss << "\nCayley table (mod " << n << ") — top-left 6×6:\n";
        int lim = std::min(n, 6);
        ss << "  ";
        for (int j = 0; j < lim; ++j)
            ss << std::setw(3) << j;
        ss << "\n";
        for (int i = 0; i < lim; ++i)
        {
            ss << std::setw(2) << i << ":";
            for (int j = 0; j < lim; ++j)
                ss << std::setw(3) << (i + j) % n;
            ss << "\n";
        }
        return ok(ss.str());
    }

