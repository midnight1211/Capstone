    AAResult polyFactor(const Poly &A, long long p)
    {
        std::ostringstream ss;
        ss << "Factorisation of " << polyStr(A) << " over GF(" << p << ")";
        int deg = (int)A.size() - 1;
        if (deg <= 0)
            return ok("constant", "No nontrivial factors");

        Poly rem = A;
        std::vector<std::pair<Poly, int>> factors;
        ss << "Trial division by degree-1 factors (x - a), a ∈ GF(" << p << "): ";
        for (long long a = 0; a < p; ++a)
        {
            int mult = 0;
            while (true)
            {
                auto ev = polyEval(rem, a, p);
                if (std::stoll(ev.value) != 0)
                    break;
                // (x - a) divides rem: perform synthetic division
                Poly factor = {mod(-a, p), 1};
                auto divres = polyDiv(rem, factor, p);
                Poly quot;
                long long cur_lead = rem.back();
                Poly r2 = rem;
                Poly q;
                while ((int)r2.size() - 1 >= 1)
                {
                    long long coeff = r2.back();
                    q.insert(q.begin(), mod(coeff, p));
                    r2.pop_back();
                    if (!r2.empty())
                        r2.back() = mod(r2.back() + coeff * a, p);
                }
                rem = q;
                mult++;
            }
            if (mult > 0)
            {
                factors.push_back({{mod(-a, p), 1}, mult});
                ss << "  (x - " << a << ")^" << mult << " ";
            }
        }
        if (!rem.empty() && (int)rem.size() - 1 > 0)
        {
            ss << "  Remaining irreducible factor: " << polyStr(rem) << " ";
        }
        if (factors.empty() && ((int)rem.size() - 1 == deg))
        {
            ss << "  Polynomial is irreducible over GF(" << p << ")";
        }
        return ok(ss.str());
    }

    AAResult quotientRing(int n, int ideal)
    {
        // Z_n / (ideal): quotient ring
        int g = mcGcd(ideal, n);
        int quotient_order = g; // |(ideal)| = n/g, so |Z_n/(ideal)| = g
        std::ostringstream ss;
        ss << "Quotient Ring Z_" << n << " / (" << ideal << ")";
        ss << "The ideal (" << ideal << ") in Z_" << n << " = (" << g << ") has order " << n / g << " ";
        ss << "Quotient ring order = |Z_" << n << "| / |(" << g << ")| = " << n << "/" << n / g << " = " << quotient_order << " ";
        ss << "Z_" << n << " / (" << g << ") ≅ Z_" << g << " ";
        ss << "Cosets: ";
        for (int i = 0; i < g; ++i)
        {
            ss << "  " << i << " + (" << g << ") = {";
            bool f = true;
            for (int k = 0; k < n; k += g)
            {
                if (!f)
                    ss << ",";
                ss << (i + k) % n;
                f = false;
            }
            ss << "}";
        }
        return ok("Z_" + cu_str(g), ss.str());
    }

    AAResult fieldExtension(int p, const Poly &minPoly)
    {
        std::ostringstream ss;
        ss << "Field Extension GF(" << p << ")[x]/(" << polyStr(minPoly) << ")\n\n";
        ss << "This quotient ring is a field iff " << polyStr(minPoly) << " is irreducible over GF(" << p << ")\n\n";
        auto irr = polyIrreducible(minPoly, p);
        ss << "Irreducibility check: " << irr.value << "\n\n";
        int deg = minPoly.size() - 1;
        long long fieldSize = 1;
        for (int i = 0; i < deg; ++i)
            fieldSize *= p;
        ss << "If irreducible: field has " << p << "^" << deg << " = " << fieldSize << " elements\n";
        ss << "Elements: polynomials of degree < " << deg << " over GF(" << p << ")\n";
        ss << "Arithmetic: add/multiply polynomials, reduce mod (" << polyStr(minPoly) << ") and mod " << p;
        return ok(ss.str());
    }

    AAResult dispatch(const std::string &op, const std::string &json)
    {
        try
        {
            if (op == "cyclic")
                return cyclic((int)getN(json, "n"));
            if (op == "dihedral")
                return dihedralGroup((int)getN(json, "n"));
            if (op == "symmetric")
                return symmetricGroup((int)getN(json, "n"));
            if (op == "is_group")
                return isGroup(cu_parseMatI(getP(json, "table")));
            if (op == "is_abelian")
                return isAbelian(parseMat(getP(json, "table")));
            if (op == "group_order")
                return groupOrder(parseMat(getP(json, "table")));
            if (op == "subgroups")
                return subgroups((int)getN(json, "n"));
            if (op == "quotient_group")
                return quotientGroup((int)getN(json, "n"), (int)getN(json, "k"));
            if (op == "direct_product")
                return directProduct((int)getN(json, "m"), (int)getN(json, "n"));
            if (op == "lagrange")
                return lagrangeThm((int)getN(json, "sub"), (int)getN(json, "grp"));
            if (op == "conjugacy")
                return conjugacyClasses(parseMat(getP(json, "table")));
            if (op == "center")
                return centerOfGroup(parseMat(getP(json, "table")));
            if (op == "commutator")
                return commutatorSubgroup(parseMat(getP(json, "table")));
            if (op == "sylow")
                return sylow((int)getN(json, "p"), (int)getN(json, "n"));
            if (op == "hom")
                return groupHomomorphism((int)getN(json, "m"), (int)getN(json, "n"), (int)getN(json, "phi"));
            if (op == "ring_zn")
                return ringZn((int)getN(json, "n"));
            if (op == "gaussian_int")
                return gaussianIntegers(getN(json, "a"), getN(json, "b"));
            if (op == "is_int_domain")
                return isIntegralDomain((int)getN(json, "n"));
            if (op == "is_field_aa")
                return isField((int)getN(json, "n"));
            if (op == "poly_add")
                return polyAdd(parsePoly(getP(json, "A")), parsePoly(getP(json, "B")), getN(json, "mod"));
            if (op == "poly_sub")
                return polySub(parsePoly(getP(json, "A")), parsePoly(getP(json, "B")), getN(json, "mod"));
            if (op == "poly_mul")
                return polyMul(parsePoly(getP(json, "A")), parsePoly(getP(json, "B")), getN(json, "mod"));
            if (op == "poly_div")
                return polyDiv(parsePoly(getP(json, "A")), parsePoly(getP(json, "B")), getN(json, "mod"));
            if (op == "poly_gcd")
                return polyGCD(parsePoly(getP(json, "A")), parsePoly(getP(json, "B")), getN(json, "mod"));
            if (op == "poly_eval")
                return polyEval(parsePoly(getP(json, "poly")), getN(json, "x"), getN(json, "mod"));
            if (op == "poly_irred")
                return polyIrreducible(parsePoly(getP(json, "poly")), getN(json, "p"));
            if (op == "cyclotomic")
                return polyCyclotomic((int)getN(json, "n"));
            if (op == "galois_field")
                return galoisField((int)getN(json, "p"), (int)getN(json, "n", 1));
            if (op == "mult_order")
                return multiplicativeOrder(getN(json, "a"), getN(json, "p"));
            if (op == "prim_element")
                return primitiveElementGF((int)getN(json, "p"));
            if (op == "perm_cycle")
                return permCycleNotation(parseVec(getP(json, "perm")));
            if (op == "perm_order")
                return permOrder(parseVec(getP(json, "perm")));
            if (op == "perm_compose")
                return permCompose(parseVec(getP(json, "p")), parseVec(getP(json, "q")));
            if (op == "perm_inverse")
                return permInverse(parseVec(getP(json, "perm")));
            if (op == "perm_parity")
                return permParity(parseVec(getP(json, "perm")));
            if (op == "perm_conjugate")
                return permConjugate(parseVec(getP(json, "p")), parseVec(getP(json, "q")));
            if (op == "field_ext")
                return fieldExtension((int)getN(json, "p"), parsePoly(getP(json, "minpoly")));
            if (op == "burnside")
            {
                std::vector<Vec> orbits;
                auto raw = getP(json, "orbits");
                size_t pos = 0;
                while ((pos = raw.find('[', pos)) != std::string::npos)
                {
                    ++pos;
                    size_t end = raw.find(']', pos);
                    if (end == std::string::npos)
                        break;
                    Vec r = parseVec("[" + raw.substr(pos, end - pos) + "]");
                    orbits.push_back(r);
                    pos = end + 1;
                }
                return burnside(orbits);
            }
            if (op == "crt_ring")
                return chineseRemainderRing(parseVec(getP(json, "mods")));
            if (op == "field_char")
                return fieldCharacteristic((int)getN(json, "p"), (int)getN(json, "n", 1));
            if (op == "ideal_gen")
                return idealGenerated((int)getN(json, "n"), (int)getN(json, "gen"));
            if (op == "orbit_stab")
                return orbitStabiliser(parseMat(getP(json, "table")), (int)getN(json, "element"));
            if (op == "poly_euclid")
                return polyEuclid(parsePoly(getP(json, "A")), parsePoly(getP(json, "B")), getN(json, "p", 2));
            if (op == "poly_factor")
                return polyFactor(parsePoly(getP(json, "poly")), getN(json, "p", 2));
            if (op == "quotient_ring")
                return quotientRing((int)getN(json, "n"), (int)getN(json, "ideal"));

            return err("Unknown abstract algebra operation: " + op);
        }
        catch (const std::exception &e)
        {
            return err(e.what());
        }
    }

