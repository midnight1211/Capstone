    AAResult orbitStabiliser(const Mat &cayley, int element)
    {
        int n = cayley.size();
        if (element >= n)
            return err("Element out of range");
        std::ostringstream ss;
        ss << "Orbit-Stabiliser Theorem for element " << element << " ";
        // The stabiliser Stab(x) = {g ∈ G : g·x = x}
        // For a group acting on itself by conjugation: Stab(x) = centraliser C(x)
        std::vector<int> stab;
        int id = 0;
        for (int i = 0; i < n; ++i)
        {
            bool isId = true;
            for (int j = 0; j < n; ++j)
                if (cayley[i][j] != j)
                {
                    isId = false;
                    break;
                }
            if (isId)
            {
                id = i;
                break;
            }
        }
        std::vector<int> inv(n);
        for (int i = 0; i < n; ++i)
            for (int j = 0; j < n; ++j)
                if (cayley[i][j] == id)
                    inv[i] = j;
        std::set<int> orbit;
        for (int g = 0; g < n; ++g)
        {
            int conj = cayley[cayley[g][element]][inv[g]];
            orbit.insert(conj);
            if (conj == element)
                stab.push_back(g);
        }
        ss << "Orbit of " << element << " (under conjugation): {";
        bool f = true;
        for (int e : orbit)
        {
            if (!f)
                ss << ",";
            ss << e;
            f = false;
        }
        ss << "}";
        ss << "Stabiliser Stab(" << element << "): {";
        f = true;
        for (int s : stab)
        {
            if (!f)
                ss << ",";
            ss << s;
            f = false;
        }
        ss << "}";
        ss << "|Orbit| × |Stab| = " << orbit.size() << " × " << stab.size() << " = " << orbit.size() * stab.size() << " = |G| = " << n << " ";
        ss << "(Orbit-Stabiliser theorem: |Orb(x)| · |Stab(x)| = |G|)";
        return ok(ss.str());
    }

