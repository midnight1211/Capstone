    AAResult permCycleNotation(const Vec &perm)
    {
        int n = perm.size();
        std::vector<bool> visited(n, false);
        std::ostringstream ss;
        ss << "Cycle notation of σ = [";
        for (int i = 0; i < n; ++i)
        {
            if (i)
                ss << ",";
            ss << perm[i];
        }
        ss << "]\n\n";
        bool isId = true;
        for (int i = 0; i < n; ++i)
            if (!visited[i])
            {
                if (perm[i] == i)
                {
                    visited[i] = true;
                    continue;
                }
                isId = false;
                ss << "(";
                int cur = i;
                while (!visited[cur])
                {
                    visited[cur] = true;
                    ss << cur;
                    cur = perm[cur];
                    if (!visited[cur])
                        ss << " ";
                }
                ss << ")";
            }
        if (isId)
            ss << "(e)  [identity]";
        return ok(ss.str());
    }

    AAResult permOrder(const Vec &perm)
    {
        int n = perm.size();
        std::vector<bool> visited(n, false);
        long long ord = 1;
        for (int i = 0; i < n; ++i)
            if (!visited[i] && perm[i] != i)
            {
                int len = 0, cur = i;
                while (!visited[cur])
                {
                    visited[cur] = true;
                    cur = perm[cur];
                    len++;
                }
                ord = ord / mcGcd(ord, len) * len;
            }
        return ok(cu_str(ord), "Order of permutation = lcm of cycle lengths = " + cu_str(ord));
    }

